OLD_EMAIL="zviki@zviki.local"
CORRECT_NAME="zvikinoza"        
CORRECT_EMAIL="znoza17@freeuni.edu.ge"

if [ "$GIT_COMMITTER_EMAIL" = "$OLD_EMAIL" ]
then
export GIT_COMMITTER_NAME="$CORRECT_NAME"
